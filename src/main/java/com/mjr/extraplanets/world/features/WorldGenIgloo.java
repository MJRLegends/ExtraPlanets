	@Override
	public boolean generate(World world, Random rand, BlockPos position) {
		int x = position.getX();
		int y = position.getY();
		int z = position.getZ();
		for (int i = 0; i < 11; i++) {
			for (int j = 0; j < 11; j++) {
				try {
					if (world.getBlockState(new BlockPos(x + i, y - 3, z + j)).getBlock() == Blocks.air)
						return false;
				} catch (Exception ex) {
					System.out.println("ExtraPlanets: " + ex.getMessage());
				}
			}
		}
		y = y - 1;
		int random = rand.nextInt(50) + 1;
		if (Config.DEBUG_MODE)
			System.out.println("Spawning Igloo at (x, y, z)" + x + " " + y + " " + z);
		if (random != 50) {
			return generateBasic(world, rand, x, y, z);
		} else {
			y = y - 3;
			return generateAdvanced(world, rand, x, y, z);
		}
	}

